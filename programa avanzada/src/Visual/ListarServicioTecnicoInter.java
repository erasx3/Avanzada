    private void tblServicioTecnicoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblServicioTecnicoMouseClicked
       int codigo;
       Tecnico unTecnico;
       List<DetalleCompraVenta> unosDetalleCompraVenta=new LinkedList<>();
       modeloDetalle.setRowCount(0);
       
       this.fila = this.tblServicioTecnico.rowAtPoint(evt.getPoint());
       
       codigo = Integer.parseInt(this.modeloTecnico.getValueAt(this.fila, 0).toString());
       unTecnico=this.miControladoraVisual.buscarServicioTecnico(codigo);
       unosDetalleCompraVenta=unTecnico.getUnosDetalleCompraVentas();
       
       Iterator<DetalleCompraVenta> itDetalle = unosDetalleCompraVenta.iterator();
       DetalleCompraVenta unDetalle;
       while(itDetalle.hasNext()){
           unDetalle=itDetalle.next();
           modeloDetalle.addRow(new Object[]{unDetalle.getCodigo(),unDetalle.getCantidad(),unDetalle.getUnConsumible().getDescripcion(),unDetalle.getSubtotal()});
       }
    }//GEN-LAST:event_tblServicioTecnicoMouseClicked

    private void cmBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmBorrarActionPerformed
       int codigo=Integer.parseInt(this.modeloTecnico.getValueAt(this.fila, 0).toString());
        try {
            this.miControladoraVisual.borrarServicioTecnico(codigo);
            refrescarVentana();
        } catch (Exception ex) {
            Logger.getLogger(ListarServicioTecnicoInter.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_cmBorrarActionPerformed


