    private void refrescarVentana() {
        this.modeloDetalle.setRowCount(0);
        this.modeloTecnico.setRowCount(0);
        cargarTablaServicio();
    }

    private void cargarTablaServicio() {
        Iterator<Tecnico> itTecnico = this.miControladoraVisual.traerServicioTecnico().iterator();
        Tecnico unTecnico;
        while (itTecnico.hasNext()) {
            unTecnico = itTecnico.next();
            modeloTecnico.addRow(new Object[]{unTecnico.getCodigo(),unTecnico.getDescripcion(),unTecnico.getSubtotal()});
        }
    }
